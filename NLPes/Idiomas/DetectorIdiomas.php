    public function analizar($texto)
    {
        $fichas = (new FichadorNatural())->fichar($texto);

        $fichasNgramas = [];

        foreach ($fichas as $ficha) {
            foreach ($this->obtenerNGramas($ficha) as $ngrama) {
                if (!isset($fichasNgramas[$ngrama])) {
                    $fichasNgramas[$ngrama] = 0;
                }

                $fichasNgramas[$ngrama]++;
            }
        }

        $fichasNgramasSuma = array_sum($fichasNgramas);
        $puntajeFinal = [];

        // Comparamos y contamos los ngramas de cada ficha contra el indice
        foreach ($fichasNgramas as $fichaNg => $fichaNgConteo) {
            if (!isset($this->indice[$fichaNg])) {
                continue;
            }

            foreach ($this->indice[$fichaNg] as $idioma => $idiomaConteo) {
                if (!isset($puntajeFinal[$idioma])) {
                    $puntajeFinal[$idioma] = 0;
                }

                // Calculamos el puntaje flotante individual y se sumamos al global
                $puntajeFinal[$idioma] += ($idiomaConteo / $this->idiomasConteo[$idioma]) * ($fichaNgConteo / $fichasNgramasSuma);
            }
        }

        // Ordenamos por puntaje de mayor a menor
        arsort($puntajeFinal);

        return $puntajeFinal;
    }

    public function detectar($texto)
    {
        $idiomasPuntaje = $this->analizar($texto);
        return key($idiomasPuntaje);
    }

    public function entrenar($texto, $idioma)
    {
        if (!isset($this->idiomasConteo[$idioma])) {
            $this->idiomasConteo[$idioma] = 0;
        }

        $fichas = (new FichadorNatural())->fichar($texto);

        foreach ($fichas as $ficha) {
            $fichaNgramas = $this->obtenerNGramas($ficha);

            foreach ($fichaNgramas as $fichaNg) {
                if (!isset($this->indice[$fichaNg])) {
                    $this->indice[$fichaNg] = [];
                }

                if (!isset($this->indice[$fichaNg][$idioma])) {
                    $this->indice[$fichaNg][$idioma] = 0;
                }

                $this->indice[$fichaNg][$idioma]++;
            }

            $this->idiomasConteo[$idioma] += count($fichaNgramas);
        }
    }

    protected function obtenerNGramas($texto, $nivel = 3)
    {
        $nGramas = [];
        $textoLen = mb_strlen($texto, 'UTF-8');

        for ($i = 0; $i < $textoLen; $i++) {
            if ($i > ($nivel - 2)) {
                $ng = '';
                for ($j = $nivel - 1; $j >= 0; $j--) {
                    $ng .= mb_substr($texto, $i - $j, 1, 'UTF-8');
                }
                $nGramas[] = $ng;
            }
        }
        return $nGramas;
    }
